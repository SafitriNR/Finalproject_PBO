    private void table_transaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_transaksiMouseClicked
        // TODO add your handling code here:
         try {
              String code_tr = table_transaksi.getValueAt(table_transaksi.getSelectedRow(), 0).toString();
            st = con.createStatement();
            Object[] judul = {"Nama Barang","Qty","Harga","Total"};
            tabmodel = new DefaultTableModel(null, judul);
            rs = st.executeQuery("select transaction_item.* , products.title, products.price from transaction_item INNER join products on transaction_item.code = products.code where transaction_item.code_transaction ='"
              + code_tr  + "'");
            while (rs.next()) {                
                Object[] data = {
                    rs.getString("title"),
                    rs.getString("qty"),
                    rs.getString("price"),
                    rs.getString("total"),
                };
                tabmodel.addRow(data);
            };
            table_detail.setModel(tabmodel);
        } catch (Exception e) {
                e.printStackTrace();

        }
    }//GEN-LAST:event_table_transaksiMouseClicked

