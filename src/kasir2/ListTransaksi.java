    private void searchingKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchingKeyReleased

        try {
            st = con.createStatement();
            tabmodel.getDataVector().removeAllElements();
            tabmodel.fireTableDataChanged();
            rs = st.executeQuery("select  code_transaction,total_belanja,bayar,kembalian,tgl, DATE_FORMAT(transaction.tgl, '%d %M %Y') as tanggal_format from transaction where code_transaction like '%" + searching.getText() +
                "%' or total_belanja like '%" + searching.getText() + "%' or bayar like '%" + searching.getText() +
                "%' or kembalian like '%" + searching.getText() +"%' or tgl like '%" + searching.getText() + "%'");
            while (rs.next()) {
                Object[] data = {
                    rs.getString("code_transaction"),
                    rs.getString("total_belanja"),
                    rs.getString("bayar"),
                    rs.getString("kembalian"),
                    rs.getString("tanggal_format"),
                    
                };
                tabmodel.addRow(data);
            };
            table_transaksi.setModel(tabmodel);
        } catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_searchingKeyReleased

