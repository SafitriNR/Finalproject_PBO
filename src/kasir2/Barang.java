    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        try {
            int jawab;

            if ((jawab = JOptionPane.showConfirmDialog(null, "Ingin menghapus data?", "konfirmasi", JOptionPane.YES_NO_OPTION)) == 0) {
                st = con.createStatement();
                st.executeUpdate("DELETE FROM products WHERE code='"
                    + tabmodel.getValueAt(table_barang.getSelectedRow(), 0) + "'");
                tampildata();
                reset();
                btn_tambah.setEnabled(true);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void searchingKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchingKeyReleased

        try {
            st = con.createStatement();
            tabmodel.getDataVector().removeAllElements();
            tabmodel.fireTableDataChanged();
            rs = st.executeQuery("select * from products where code like '%" + searching.getText() +
                "%' or title like '%" + searching.getText() + "%' or price like '%" + searching.getText() +
                "%'");
            while (rs.next()) {
                Object[] data = {
                    rs.getString("code"),
                    rs.getString("title"),
                    rs.getString("price")
                };
                tabmodel.addRow(data);
            };
            table_barang.setModel(tabmodel);
        } catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_searchingKeyReleased

