    public void tampildata(){
        try {
            st = con.createStatement();
            Object[] judul = {"Code","Nama","Harga"};
            tabmodel = new DefaultTableModel(null, judul);
            rs = st.executeQuery("select * from products order by id desc");
            while (rs.next()) {                
                Object[] data = {
                    rs.getString("code"),
                    rs.getString("title"),
                    rs.getString("price"),
                };
                tabmodel.addRow(data);
            };
            table_barang.setModel(tabmodel);
        } catch (Exception e) {
                e.printStackTrace();

        }
    }
